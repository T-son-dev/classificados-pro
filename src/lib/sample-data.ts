// Helper to generate display settings based on plan
function getDisplaySettings(planType: PlanType) {
  const plan = plans.find((p) => p.type === planType);
  if (!plan) return defaultDisplaySettings;

  const badges = [];
  if (plan.features.featuredBadge) badges.push({ type: "featured" as const, label: "Destaque", color: "amber" });
  if (plan.features.verifiedBadge) badges.push({ type: "verified" as const, label: "Verificado", color: "blue" });

  return {
    priority: plan.displayPriority,
    displayProbability: plan.features.displayProbability,
    badges,
    style: {
      boldTitle: plan.features.boldTitle,
      backgroundColor: plan.features.coloredBackground ? "#fffbeb" : undefined,
      borderColor: plan.features.borderHighlight ? "#f59e0b" : undefined,
      thumbnailSize: plan.features.largerThumbnail ? "large" as const : "normal" as const,
    },
  };
}

const defaultDisplaySettings = {
  priority: 10,
  displayProbability: 0.4,
  badges: [],
  style: {
    boldTitle: false,
    thumbnailSize: "normal" as const,
  },
};

